public class Copyright {
    private int id;
    private String copyright_id;
    private String copyright_name;
    private String copyright_info;
    private String copyright_department;
    private String copyright_company;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCopyright_id() {
        return copyright_id;
    }

    public void setCopyright_id(String copyright_id) {
        this.copyright_id = copyright_id;
    }

    public String getCopyright_name() {
        return copyright_name;
    }

    public void setCopyright_name(String copyright_name) {
        this.copyright_name = copyright_name;
    }

    public String getCopyright_info() {
        return copyright_info;
    }

    public void setCopyright_info(String copyright_info) {
        this.copyright_info = copyright_info;
    }

    public String getCopyright_department() {
        return copyright_department;
    }

    public void setCopyright_department(String copyright_department) {
        this.copyright_department = copyright_department;
    }

    public String getCopyright_company() {
        return copyright_company;
    }

    public void setCopyright_company(String copyright_company) {
        this.copyright_company = copyright_company;
    }
}
