public class Building {
    private int id;
    private String building_id;
    private String building_name;
    private String building_status;
    private String building_info;
    private String building_department;
    private String building_company;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBuilding_id() {
        return building_id;
    }

    public void setBuilding_id(String building_id) {
        this.building_id = building_id;
    }

    public String getBuilding_name() {
        return building_name;
    }

    public void setBuilding_name(String building_name) {
        this.building_name = building_name;
    }

    public String getBuilding_status() {
        return building_status;
    }

    public void setBuilding_status(String building_status) {
        this.building_status = building_status;
    }

    public String getBuilding_info() {
        return building_info;
    }

    public void setBuilding_info(String building_info) {
        this.building_info = building_info;
    }

    public String getBuilding_department() {
        return building_department;
    }

    public void setBuilding_department(String building_department) {
        this.building_department = building_department;
    }

    public String getBuilding_company() {
        return building_company;
    }

    public void setBuilding_company(String building_company) {
        this.building_company = building_company;
    }
}
