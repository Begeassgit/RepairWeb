package com.repair.web.Entity;

public class Supervisor {
    private int super_id;
    private String super_username;
    private String super_password;
    private String super_phone;
    private String super_company;
    private String super_department;

    public int getSuper_id() {
        return super_id;
    }

    public void setSuper_id(int super_id) {
        this.super_id = super_id;
    }

    public String getSuper_username() {
        return super_username;
    }

    public void setSuper_username(String super_username) {
        this.super_username = super_username;
    }

    public String getSuper_password() {
        return super_password;
    }

    public void setSuper_password(String super_password) {
        this.super_password = super_password;
    }

    public String getSuper_phone() {
        return super_phone;
    }

    public void setSuper_phone(String super_phone) {
        this.super_phone = super_phone;
    }

    public String getSuper_company() {
        return super_company;
    }

    public void setSuper_company(String super_company) {
        this.super_company = super_company;
    }

    public String getSuper_department() {
        return super_department;
    }

    public void setSuper_department(String super_department) {
        this.super_department = super_department;
    }
}
