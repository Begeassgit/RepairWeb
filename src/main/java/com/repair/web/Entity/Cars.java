public class Cars {
    private int id;
    private String cars_id;
    private String cars_name;
    private String cars_brand;
    private String cars_status;
    private String cars_department;
    private String cars_company;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCars_id() {
        return cars_id;
    }

    public void setCars_id(String cars_id) {
        this.cars_id = cars_id;
    }

    public String getCars_name() {
        return cars_name;
    }

    public void setCars_name(String cars_name) {
        this.cars_name = cars_name;
    }

    public String getCars_brand() {
        return cars_brand;
    }

    public void setCars_brand(String cars_brand) {
        this.cars_brand = cars_brand;
    }

    public String getCars_status() {
        return cars_status;
    }

    public void setCars_status(String cars_status) {
        this.cars_status = cars_status;
    }

    public String getCars_department() {
        return cars_department;
    }

    public void setCars_department(String cars_department) {
        this.cars_department = cars_department;
    }

    public String getCars_company() {
        return cars_company;
    }

    public void setCars_company(String cars_company) {
        this.cars_company = cars_company;
    }
}
