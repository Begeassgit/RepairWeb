package com.repair.web.Entity;

public class Items {
    private int id;
    private String items_id;
    private String items_name;
    private String items_type;
    private String items_brand;
    private String items_count;
    private String items_info;
    private String items_department;
    private String items_company;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getItems_id() {
        return items_id;
    }

    public void setItems_id(String items_id) {
        this.items_id = items_id;
    }

    public String getItems_name() {
        return items_name;
    }

    public void setItems_name(String items_name) {
        this.items_name = items_name;
    }

    public String getItems_brand() {
        return items_brand;
    }

    public void setItems_brand(String items_brand) {
        this.items_brand = items_brand;
    }

    public String getItems_count() {
        return items_count;
    }

    public void setItems_count(String items_count) {
        this.items_count = items_count;
    }

    public String getItems_info() {
        return items_info;
    }

    public void setItems_info(String items_info) {
        this.items_info = items_info;
    }

    public String getItems_department() {
        return items_department;
    }

    public void setItems_department(String items_department) {
        this.items_department = items_department;
    }

    public String getItems_company() {
        return items_company;
    }

    public void setItems_company(String items_company) {
        this.items_company = items_company;
    }

    public String getItems_type() {
        return items_type;
    }

    public void setItems_type(String items_type) {
        this.items_type = items_type;
    }
}
